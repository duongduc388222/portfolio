import { formatReadingTime, formatWordCount } from '@/lib/readingTime';
import { PostFrontmatter } from '@/lib/mdx';
import TagPill from './TagPill';

interface PostMetaProps {
  frontmatter: PostFrontmatter;
  readingTime: {
    minutes: number;
    words: number;
  };
  showTags?: boolean;
  showReadingTime?: boolean;
  showWordCount?: boolean;
  className?: string;
}

export default function PostMeta({
  frontmatter,
  readingTime,
  showTags = true,
  showReadingTime = true,
  showWordCount = true,
  className = '',
}: PostMetaProps) {
  const formatDate = (dateString: string) => {
    const date = new Date(dateString);
    return date.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
    });
  };

  return (
    <div className={`flex flex-wrap items-center gap-4 text-sm text-gray-300 ${className}`}>
      {/* Date */}
      <time dateTime={frontmatter.date} className="flex items-center gap-1">
        <svg
          className="w-4 h-4"
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
          xmlns="http://www.w3.org/2000/svg"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={2}
            d="M8 7V3m8 4V3m-9 8h10M5 21h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v12a2 2 0 002 2z"
          />
        </svg>
        {formatDate(frontmatter.date)}
      </time>

      {/* Reading Time */}
      {showReadingTime && (
        <span className="flex items-center gap-1">
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z"
            />
          </svg>
          {formatReadingTime(readingTime.minutes)}
        </span>
      )}

      {/* Word Count */}
      {showWordCount && (
        <span className="flex items-center gap-1">
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z"
            />
          </svg>
          {formatWordCount(readingTime.words)}
        </span>
      )}

      {/* Author */}
      {frontmatter.author && (
        <span className="flex items-center gap-1">
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M16 7a4 4 0 11-8 0 4 4 0 018 0zM12 14a7 7 0 00-7 7h14a7 7 0 00-7-7z"
            />
          </svg>
          {frontmatter.author}
        </span>
      )}

      {/* Tags */}
      {showTags && frontmatter.tags.length > 0 && (
        <div className="flex items-center gap-2">
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M7 7h.01M7 3h5c.512 0 1.024.195 1.414.586l7 7a2 2 0 010 2.828l-7 7a2 2 0 01-2.828 0l-7-7A1.994 1.994 0 013 12V7a4 4 0 014-4z"
            />
          </svg>
          <div className="flex flex-wrap gap-1">
            {frontmatter.tags.map((tag) => (
              <TagPill key={tag} tag={tag} size="sm" variant="ghost" />
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
